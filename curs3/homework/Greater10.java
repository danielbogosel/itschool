class Greater10{
	public static void main(String[] args) {
		int valCompare=10;

		if(valCompare<10){
			System.out.println("Numarul introdus este mai mic ca 10");

		} else if(valCompare>10){
			System.out.println("Numarul introdus este mai mic ca 10");

		}else if(valCompare==10){
			System.out.println("Numarul introdus este = cu 10");

		}
	}
}